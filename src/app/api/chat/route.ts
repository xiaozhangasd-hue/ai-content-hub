export const runtime = "nodejs";
export const dynamic = "force-dynamic";

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { messages, merchantInfo } = body;

    if (!messages || !Array.isArray(messages) || messages.length === 0) {
      return new Response(JSON.stringify({ error: "请提供消息内容" }), {
        status: 400,
        headers: { "Content-Type": "application/json" },
      });
    }

    // 构建上下文
    let contextPrompt = "";
    
    if (merchantInfo && merchantInfo.name) {
      contextPrompt = `\n\n## 当前用户信息
- 机构名称：${merchantInfo.name}
- 机构类型：${merchantInfo.category || "未设置"}
- 主要课程：${merchantInfo.subjects?.join("、") || "未设置"}
- 目标年龄：${merchantInfo.targetAge || "未设置"}
- 所在城市：${merchantInfo.city || "未设置"}
- 特色优势：${merchantInfo.features?.join("、") || "未设置"}
- 教育理念：${merchantInfo.philosophy || "未设置"}

你要基于这些信息给用户提供个性化的建议和内容。如果信息不完整，可以主动询问。`;
    }

    const client = new AIClient();

    // 构建完整的消息列表
    const fullMessages: ChatMessage[] = [
      { role: "system", content: SYSTEM_PROMPT + contextPrompt },
      ...messages.map((msg: { role: string; content: string }) => ({
        role: msg.role as "user" | "assistant",
        content: msg.content,
      })),
    ];

    // 返回流式响应
    return client.createStreamResponse(fullMessages, {
      temperature: 0.8,
      maxTokens: 1500,
    });
  } catch (error) {
    console.error("聊天API错误:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "服务错误" }),
      {
        status: 500,
        headers: { "Content-Type": "application/json" },
      }
    );
  }
}
